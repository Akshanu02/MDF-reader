# Define the path to the .dat file
dat_file_path = "smaller_file_1.dat"

# Create a list to store CAN messages
can_messages = []

# Read the binary data from the .dat file
with open(dat_file_path, 'rb') as file:
    binary_data = file.read()

# Parse the binary data into CAN messages
offset = 0
while offset < len(binary_data):
    try:
        # Extract timestamp, arbitration_id (ID), extended_id, data_length (dl), and data from binary data
        timestamp, arbitration_id, extended_id, dl = struct.unpack("<IQBB", binary_data[offset:offset+17])
        data = binary_data[offset+17:offset+17+dl]
        
        # Create a CAN message using the extracted data
        msg = can.Message(
            timestamp=timestamp / 1e6,  # Convert timestamp to seconds
            arbitration_id=arbitration_id,
            is_extended_id=bool(extended_id),
            dlc=dl,
            data=data
        )
        
        can_messages.append(msg)
        
        offset += 17 + dl
    except struct.error:
        break


# Function to extract signal values based on signal group name
def extract_signal_values(group_name, messages):
    signals = None
    for group in signal_groups:
        if group["name"] == group_name:
            signals = group["signals"]
            break

    if signals is None:
        return None

    signal_values = {}
    for signal in signals:
        for msg in messages:
            if signal.encode() in msg.data:
                value = msg.data[msg.data.index(signal.encode()) + len(signal.encode()):]
                signal_values[signal] = {
                    "timestamp": msg.timestamp,
                    "id": msg.arbitration_id,
                    "direction": "Rx" if msg.is_extended_id else "Tx",
                    "dl": len(msg.data),
                    "channel": msg.channel_info
                }

    return signal_values

# Create an ASC file to store the extracted signal values with timestamps and groups
asc_file_path = "output.asc"
with open(asc_file_path, 'w') as asc_file:
    # Loop through the signal groups and extract the signal values
    for group in signal_groups:
        group_name = group["name"]
        signal_values = extract_signal_values(group_name, can_messages)

        if signal_values:
            asc_file.write(f"{group_name} Signal Values:\n")
            for signal, value_info in signal_values.items():
                asc_file.write(f"Timestamp: {value_info['timestamp']}  ID: {value_info['id']}  {value_info['direction']}  DL: {value_info['dl']}  Channel: {value_info['channel']}\n")
                asc_file.write(f"{signal}: {value}\n")
            asc_file.write("\n")
        else:
            asc_file.write(f"No data found for {group_name}\n")

print(f"Data extracted and saved to {asc_file_path}")
